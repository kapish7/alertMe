package com.example.alertme.models;

public class AlarmTone 
{
	private String alarmTone;
	private int alarmVolumeLevel;
	
	public String getAlarmTone() {
		return alarmTone;
	}

	public void setAlarmTone(String alarmTone) {
		this.alarmTone = alarmTone;
	}
	
	public int getAlarmVolumeLevel() {
		return alarmVolumeLevel;
	}

	public void setAlarmVolumeLevel(int alarmVolumeLevel) {
		this.alarmVolumeLevel = alarmVolumeLevel;
	}



}
